import streamlit as st
from main import solve_problem

st.set_page_config(page_title="AI Project Assistant", layout="wide")
st.title("ğŸ§  IT Project Team Simulator")

# Input field
problem = st.text_area("ğŸ“ Describe your IT project/problem/task:", height=200)

# Agent selection
agent_option = st.radio(
    "ğŸ‘¥ Choose agent(s) to simulate:",
    ("Business Analyst", "Designer", "Developer", "Tester", "All Agents")
)

agent_map = {
    "Business Analyst": ["business_analyst"],
    "Designer": ["designer"],
    "Developer": ["developer"],
    "Tester": ["tester"],
    "All Agents": ["business_analyst", "designer", "developer", "tester"]
}
selected_roles = agent_map[agent_option]

if st.button("ğŸš€ Run Simulation"):
    if not problem.strip():
        st.warning("âš ï¸ Please enter a project or task description.")
    else:
        with st.spinner("ğŸ¤– Agents are working together..."):
            result = solve_problem(problem, selected_roles)

            st.success("âœ… Output Generated!")
            st.markdown("### ğŸ“Œ Team Output")

            if isinstance(result, dict):
                for key, val in result.items():
                    st.subheader(f"ğŸ”¹ {key}")
                    if isinstance(val, dict):
                        st.markdown(f"**Agent:** `{val.get('agent', 'Unknown')}`")
                        st.code(val.get('output', 'No output'), language='markdown')
                    else:
                        st.markdown(val)
            else:
                st.code(result, language='markdown')









